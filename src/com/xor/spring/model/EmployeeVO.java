package com.xor.spring.model;

public class EmployeeVO {

	private int id;
	private String name;
	private String empDetailBeanId;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmpDetailBeanId() {
		return empDetailBeanId;
	}

	public void setEmpDetailBeanId(String empDetailBeanId) {
		this.empDetailBeanId = empDetailBeanId;
	}

}
